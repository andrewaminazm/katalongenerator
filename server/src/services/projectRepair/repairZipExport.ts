import fs from "node:fs/promises";
import path from "node:path";
import AdmZip from "adm-zip";
import { resolveProjectRoot } from "../projectIntelligenceV2/sourceLoader.js";
import { loadProjectIndex } from "../projectIntelligence/index.js";
import type { RepairDiff } from "./types.js";
import { repairZipPath } from "./cache.js";

function normalizeRel(p: string): string {
  return p.replace(/\\/g, "/").replace(/^\.\//, "");
}

function buildRepairMap(diffs: RepairDiff[]): Map<string, string> {
  const map = new Map<string, string>();
  for (const d of diffs) {
    if (!d.changed) continue;
    const key = normalizeRel(d.filePath);
    map.set(key, d.repaired);
    const base = key.split("/").pop();
    if (base) map.set(base, d.repaired);
  }
  return map;
}

function lookupRepaired(rel: string, repairMap: Map<string, string>): string | undefined {
  const n = normalizeRel(rel);
  if (repairMap.has(n)) return repairMap.get(n);
  const base = n.split("/").pop();
  if (base && repairMap.has(base)) return repairMap.get(base);
  for (const [k, v] of repairMap) {
    if (n.endsWith(k) || k.endsWith(n)) return v;
  }
  return undefined;
}

async function walkFiles(root: string): Promise<string[]> {
  const out: string[] = [];
  async function walk(dir: string) {
    const entries = await fs.readdir(dir, { withFileTypes: true });
    for (const ent of entries) {
      const full = path.join(dir, ent.name);
      if (ent.isDirectory()) {
        await walk(full);
      } else {
        out.push(normalizeRel(path.relative(root, full)));
      }
    }
  }
  await walk(root);
  return out;
}

function repairManifest(diffs: RepairDiff[], projectName: string): string {
  const changed = diffs.filter((d) => d.changed);
  const lines = [
    `# Repaired Katalon Project — ${projectName}`,
    "",
    `Generated by AI Project Repair Engine.`,
    "",
    "## Applied script repairs",
    "",
    ...(changed.length === 0
      ? ["No auto-applicable script changes were applied. Review suggestions in the platform UI."]
      : changed.map(
          (d) =>
            `- **${d.filePath}** (${d.category}): ${d.diffSummary.slice(0, 3).join("; ") || "updated"}`
        )),
    "",
    "## Important",
    "",
    "- Import this project into Katalon Studio and run your suites to validate.",
    "- Locator and architecture suggestions are NOT auto-written to OR files in this zip.",
    "- Compare with your original project before promoting to production.",
    "",
  ];
  return lines.join("\n");
}

export async function buildRepairedProjectZip(input: {
  projectId: string;
  repairId: string;
  diffs: RepairDiff[];
  projectName: string;
}): Promise<string | null> {
  const index = await loadProjectIndex(input.projectId);
  if (!index) return null;

  const zipPath = repairZipPath(input.repairId);
  await fs.mkdir(path.dirname(zipPath), { recursive: true });

  const zip = new AdmZip();
  const repairMap = buildRepairMap(input.diffs);
  const root = await resolveProjectRoot(input.projectId, index);

  if (root) {
    const files = await walkFiles(root);
    for (const rel of files) {
      const abs = path.join(root, rel.split("/").join(path.sep));
      const repaired = lookupRepaired(rel, repairMap);
      let content: Buffer;
      if (repaired !== undefined) {
        content = Buffer.from(repaired, "utf8");
      } else {
        content = await fs.readFile(abs);
      }
      zip.addFile(rel, content);
    }
  } else {
    for (const d of input.diffs.filter((x) => x.changed)) {
      zip.addFile(d.filePath, Buffer.from(d.repaired, "utf8"));
    }
  }

  zip.addFile(
    "REPAIR_MANIFEST.md",
    Buffer.from(repairManifest(input.diffs, input.projectName), "utf8")
  );

  if (input.diffs.filter((d) => d.changed).length > 0) {
    const patchDir = "ai-repair/patches";
    for (const d of input.diffs.filter((x) => x.changed)) {
      const safeName = d.filePath.replace(/[^a-zA-Z0-9._-]/g, "_");
      zip.addFile(`${patchDir}/${safeName}.original.groovy`, Buffer.from(d.original, "utf8"));
      zip.addFile(`${patchDir}/${safeName}.repaired.groovy`, Buffer.from(d.repaired, "utf8"));
    }
  }

  zip.writeZip(zipPath);
  return zipPath;
}

export async function readRepairedZipBuffer(repairId: string): Promise<Buffer | null> {
  try {
    return await fs.readFile(repairZipPath(repairId));
  } catch {
    return null;
  }
}
